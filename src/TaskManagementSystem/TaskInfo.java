    private void editTaskBtnText2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editTaskBtnText2MouseClicked
        // Display the edit task
        EditTask editTaskUi= new EditTask();
        editTaskUi.setVisible(true);
        
        // copy all the data from the table to the edit task page
        try{
            EditTask.taskCategoryField.setText(taskCategoryLabel.getText());
            EditTask.taskNameField.setText(taskNameLabel.getText());
            
            // get string from Task Info Form
            String date = deadlineLabel.getText();
            
            // convert string into date
            try {
                date1 = new SimpleDateFormat("yyyy-mm-dd").parse(date);
                System.out.println(date1);
                EditTask.deadlineDateField.setDate(date1);
            } catch (ParseException ex) {
                Logger.getLogger(DashboardMain.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            EditTask.taskPriorityDropDown.setSelectedItem(taskPriorityLabel.getText());
            EditTask.statusDropDown.setSelectedItem(statusLabel.getText());
            EditTask.descriptionTextArea.setText(descriptionLabel.getText());
            EditTask.taskIdField.setText(idField.getText());

        } catch (Exception e){
            System.out.println(e);
        }

    }//GEN-LAST:event_editTaskBtnText2MouseClicked

