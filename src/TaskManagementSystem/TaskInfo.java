    private void deleteBtnLabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteBtnLabelMouseClicked
        // Delete action
        
         // Delete the data if some data is clicked
        
        int row = dashboardTable.getSelectedRow();
        Statement ps;
        
        // check if a row is selected
        if(row >= 0){
            try{
                int taskIdTable = Integer.parseInt(dashboardTable.getModel().getValueAt(row, 6).toString());
                String deleteQuery = "DELETE FROM `task_list` WHERE `task_list`.`task_id` = " + taskIdTable;
                ps = registerUserCon.getConnection().createStatement();
                ps.executeUpdate(deleteQuery);
                
                // update ui 
                model.removeRow(row);
                JOptionPane.showMessageDialog(null, "Task Deleted!");
            } catch (Exception e){
                JOptionPane.showMessageDialog(null, "Task Deleted!");
                System.out.println(e);
            }
        } 
            
        
    }//GEN-LAST:event_deleteBtnLabelMouseClicked

